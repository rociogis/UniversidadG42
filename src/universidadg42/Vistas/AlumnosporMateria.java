     private void cargarComboBox(){
      MateriaData Mdata = new MateriaData();
      List<Materia> materias = new ArrayList();
      materias = Mdata.listarMaterias();
      
      for(Materia aux : materias){
          jcbMaterias.addItem(aux);
      }
    }
    
    private void borrarFilas() {
        DefaultTableModel modelo = (DefaultTableModel) jtAlumnos.getModel();
        int f = jtAlumnos.getRowCount() - 1;
        for (; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }
    
    private void armarCabeceraTabla(){
        modelo.addColumn("IdAlumno");
        modelo.addColumn("Dni");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nombre");
        
        jtAlumnos.setModel(modelo);
    }
