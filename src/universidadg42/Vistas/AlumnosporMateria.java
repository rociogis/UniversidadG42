    private void jcbMateriasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbMateriasActionPerformed
        // TODO add your handling code here:
        borrarFilas();
        Materia materiaSeleccionado = new Materia();
        materiaSeleccionado = (Materia) jcbMaterias.getSelectedItem();
        InscripcionData Idata = new InscripcionData();
        List<Alumno> alumnos = new ArrayList();
        alumnos = Idata.obtenerAlumnosPorMateria(materiaSeleccionado.getIdMateria());
        for(Alumno aux : alumnos){
               modelo.addRow(new Object[]{aux.getIdAlumno(), aux.getDni(), aux.getApellido(), aux.getNombre()});
           }
    }//GEN-LAST:event_jcbMateriasActionPerformed


